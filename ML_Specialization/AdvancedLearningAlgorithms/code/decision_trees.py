# Entropy
def compute_entropy(y):
    """
    Computes the entropy for 
    
    Args:
       y (ndarray): Numpy array indicating whether each example at a node is
           edible (`1`) or poisonous (`0`)
       
    Returns:
        entropy (float): Entropy at that node
        
    """
    entropy = 0.

    if not len(y):
        return 0
    
    p1 = sum(y == 1)/len(y)
    
    if p1 == 0 or p1 == 1:
        return 0
    
    entropy = - p1 * np.log2(p1) - (1- p1) * np.log2(1 - p1)    
        
    return entropy

# Split dataset
def split_dataset(X, node_indices, feature):
    """
    Splits the data at the given node into
    left and right branches
    
    Args:
        X (ndarray):             Data matrix of shape(n_samples, n_features)
        node_indices (list):     List containing the active indices. I.e, the samples being considered at this step.
        feature (int):           Index of feature to split on
    
    Returns:
        left_indices (list):     Indices with feature value == 1
        right_indices (list):    Indices with feature value == 0
    """

    left_indices = []
    right_indices = []
    
    for i in node_indices:
        if X[i, feature]:
            left_indices.append(i)
        else:
            right_indices.append(i)
        
    return left_indices, right_indices

# Information gain
def compute_information_gain(X, y, node_indices, feature):
    
    """
    Compute the information of splitting the node on a given feature
    
    Args:
        X (ndarray):            Data matrix of shape(n_samples, n_features)
        y (array like):         list or ndarray with n_samples containing the target variable
        node_indices (ndarray): List containing the active indices. I.e, the samples being considered in this step.
   
    Returns:
        cost (float):        Cost computed
    
    """    
    # Split dataset
    left_indices, right_indices = split_dataset(X, node_indices, feature)
    

    X_node, y_node = X[node_indices], y[node_indices]
    X_left, y_left = X[left_indices], y[left_indices]
    X_right, y_right = X[right_indices], y[right_indices]
    information_gain = 0

    w_left = len(left_indices) / len(node_indices)
    w_right = len(right_indices) / len(node_indices)
    h_node = compute_entropy(y_node)
    h_left = compute_entropy(y_left)
    h_right = compute_entropy(y_right)
    
    information_gain = h_node - (w_left * h_left + w_right * h_right)
    
    return information_gain

# Get Best Split
def get_best_split(X, y, node_indices):   
    """
    Returns the optimal feature and threshold value
    to split the node data 
    
    Args:
        X (ndarray):            Data matrix of shape(n_samples, n_features)
        y (array like):         list or ndarray with n_samples containing the target variable
        node_indices (ndarray): List containing the active indices. I.e, the samples being considered in this step.

    Returns:
        best_feature (int):     The index of the best feature to split
    """    
    
    num_features = X.shape[1]
    best_feature = 0
    
    # If pure, return -1
    if sum(y[node_indices] == 1) == len(node_indices) or sum(y[node_indices] == 1) == 0:
        return -1
    
    for i in range(num_features):
        if compute_information_gain(X, y, node_indices, i) > compute_information_gain(X, y, node_indices, best_feature):
            best_feature = i
   
    return best_feature

